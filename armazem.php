<h1><strong>ARMAZÉM</strong></h1>
<fieldset>
        <img src="../imagens/Bandeira_de_Armazém.jpg">
        <h2>Origens e povoamento</h2>
        <p>Em Santa Catarina, os imigrantes que vieram dos Açores colonizaram, em primeiro lugar, as regiões localizadas 
          no litoral, (o fato, é salientado, tendo validade para a totalidade do Brasil), e, em segundo lugar, o planalto.
          Pórtico de entrada de Armazém
          No momento em que nessas regiões eram estabelecidos os vicentistas e açorianos, eram fixados no planalto, durante 
          uma grande quantidade de anos após, paulistas de Piratininga e de demais regiões. Havia dificuldade e lentidão 
          entre ambas as áreas intercambiadas, por causa das serras do Mar e Geral, muralhadas, que se interpõem entre as 
          duas. O primeiro caminho, era feito da região de Laguna até os Campos de Lages. Era a Estrada dos Conventos. Por 
          ali ocorria o trânsito dos tropeiros, que desciam da "Serra", com produtos pastoris e sendo os tropeiros, 
          propriamente ditos, condutores, no caminho de volta, de artigos de primeira necessidade (sal, tecidos, querosene, 
          etc).
          O município passou a chamar-se Armazém, porque ali existia uma casa comercial de menor porte que se estabeleceu
           nos "Sertões dos Corrêas" (os Corrêas seriam os primeiros habitantes de Armazém), ponto de frequência dos 
           "Serranos".</p>
        <p>A história do município, é prendida, de modo íntimo, à de Tubarão, da qual era distrito em 1901. De acordo 
          com a Lei nº 380, de 19 de dezembro de 1958, era criando o município de Armazém, com território que se
          desmembrou de Tubarão. O novo município foi instalado em 28 de janeiro de 1959.[6]
          O prefeito escolhido sob nomeação do governo estadual foi o senhor José Diomário da Rosa e o senhor Paulo Wensing, 
          o prefeito que venceu as primeiras eleições municipais</p>
        <p>Armazém está a 30 km de Tubarão e da BR-101. Para chegar, vindo do sul ou do norte do estado, siga pela BR 101 e 
          tome direção oeste pela SC-438 hoje SC 370 até Gravatal, seguindo então pela SC-431 hoje SC 435. Para quem vem do 
          oeste, o melhor caminho é a rota Lages - São Joaquim - Gravatal, seguindo pela mesma SC-438 atual SC 370.</p>

